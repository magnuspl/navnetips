export function getCategoryContent(category: 'boy' | 'girl' | 'dog' | 'cat', nameCategory?: string): string {
  // Return main category content if no specific category is provided
  if (!nameCategory) {
    switch (category) {
      case 'boy':
        return marked(guttenavnMain);
      case 'girl':
        return marked(jentenavnMain);
      case 'dog':
        return marked(dogNames);
      case 'cat':
        return marked(catNames);
      default:
        return '';
    }
  }

  // Return specific subcategory content
  const content = {
    boy: {
      norrønt: boyNorseNames,
      klassisk: boyClassicNames,
      moderne: boyModernNames,
      unikt: boyUniqueNames,
    },
    girl: {
      norrønt: girlNorseNames,
      klassisk: girlClassicNames,
      moderne: girlModernNames,
      unikt: girlUniqueNames,
    },
  };

  return marked(content[category]?.[nameCategory as keyof typeof content[typeof category]] || '');
}